Auf dieser Seite haben Sie die Möglichkeit, einen Keltertermin online zu vereinbaren. Suchen Sie sich Ihren
Wunschtermin aus und klicken auf „buchen“.
<p>
Sie werden aufgefordert, Name, Telefonnummer sowie die Menge des zu kelternden Obstes einzugeben und mit
„buchen“ zu bestätigen. Unsere Anlage verarbeitet in der Stunde ca. 7-8  Zentner (1 Zentner = 50Kg).
<p>
Das Buchungsystem prüft, ob Ihre Menge zum Wunschtermin verarbeitet werden kann. Sollte das der Fall sein,
erscheint die Terminseite und Ihr Termin ist als „belegt“ rot markiert und damit verbindlich gebucht.
<p>
Bitte beachten Sie:
<ul>
<li>Geben Sie die Menge bei der Buchung möglichst genau an. Größere Mengen können an Ihrem Wunschtermin nicht
	verarbeitet werden.</li>
<li>Wollen Sie Birnen, Quitten oder Trauben keltern, ist es ratsam, den ersten Termin am Tag zu buchen, weil ein
	Reinigen der Anlage im laufenden Betrieb nicht möglich ist.</li>
<li>Eine Terminbuchung ist verbindlich und kann nur telefonisch oder per Email spätestens einen Tag vor
	dem Termin storniert werden. Ansonsten fallen 50% der Keltergebühren an.</li>
<li>Wir bitten, Termine für Mengen über 20 Zentner = 1000Kg weiterhin telefonisch zu vereinbaren.</li>
</ul>
